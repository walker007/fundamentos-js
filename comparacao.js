console.log("========= Operadores de Comparação ==========");
var comparador1 = 15,
    comparador2 = 10;

console.log(`${comparador1} é igual ${comparador2}?`, comparador1 == comparador2);
console.log(`${comparador1} é igual e possui o mesmo tipo de ${comparador2}?`, comparador1 === comparador2);
console.log(`${comparador1} é maior que ${comparador2}? `, comparador1 > comparador2);
console.log(`${comparador1} é menor que ${comparador2}?`, comparador1 < comparador2);
console.log(`${comparador1} é maior ou igual a ${comparador2}?`, comparador1 >= comparador2);
console.log(`${comparador1} é menor ou igual a ${comparador2}?`, comparador1 <= comparador2);